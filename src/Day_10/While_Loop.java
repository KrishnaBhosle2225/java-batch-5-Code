package Day_10;

public class While_Loop {

    public static void main(String[] args) {

//        while-loop
//        initialization, condition,incre/decre

//        1 to 10 print
        int i = 1;//initialization

        //condition
        while(i <=10) {

            System.out.println(i);
            i++;//incre/decre
        }
        System.out.println("last line");
    }
}
