package data_types;

public class Basics {

    public static void main(String[] args) {

        //byte data type 1 byte (8 bit)
//        range -128 to 127
        byte a = 12;
        System.out.println(a);

//        short takes memory of size 2 bytes
        short b = 128;
        System.out.println(b);

        //int is of 4byte memory (4*8)
        int roll_no = 324;
        System.out.println(roll_no);

        int money = 498357894;
        System.out.println(money);

//        long is a another data types -> 8 bytes (8*8)
        long views = 893825676574343l;


    }
}
